void set_pwm(int8_t duty) {
  if (duty < -100) {
    duty = -100;
  }
  if (duty > 100) {
    duty = 100;
  }
  uint32_t extended_duty;
  if (duty < 0) {
    extended_duty = htim1.Init.Period * -1 * duty;
    htim1.Instance->CCR1 = extended_duty / 100;
    htim1.Instance->CCR2 = 0;
  } else {
    extended_duty = htim1.Init.Period * duty;
    htim1.Instance->CCR1 = 0;
    htim1.Instance->CCR2 = extended_duty / 100;
  }

}
